def export_docx(
    meeting_title: str,
    meeting_date: Any,
    participants: List[str],
    summary_text: str,
    decisions: List[str],
    action_items: List[dict],
    transcript_text: str,
) -> bytes:
    from docx import Document
    from docx.shared import Pt, RGBColor, Inches
    from docx.enum.text import WD_ALIGN_PARAGRAPH

    doc = Document()

    # Set page margins
    for section in doc.sections:
        section.top_margin = Inches(1)
        section.bottom_margin = Inches(1)
        section.left_margin = Inches(1.2)
        section.right_margin = Inches(1.2)

    BLUE = RGBColor(0x1D, 0x4E, 0xD8)
    DARK = RGBColor(0x11, 0x18, 0x27)
    GRAY = RGBColor(0x6B, 0x72, 0x80)

    def add_heading(text: str, level: int = 1):
        h = doc.add_heading(text, level=level)
        for run in h.runs:
            run.font.color.rgb = BLUE

    def add_body(text: str):
        p = doc.add_paragraph(text)
        for run in p.runs:
            run.font.size = Pt(10)
            run.font.color.rgb = DARK

    # Title
    t = doc.add_heading(meeting_title, 0)
    for run in t.runs:
        run.font.color.rgb = BLUE
        run.font.size = Pt(20)

    doc.add_paragraph(f"Starlight Secondary School, Amuria District, Uganda").style = "Subtitle"
    doc.add_paragraph(f"Date: {_format_date(meeting_date)}")
    if participants:
        doc.add_paragraph(f"Attendees: {', '.join(participants)}")

    doc.add_paragraph()

    add_heading("Executive Summary", 1)
    add_body(summary_text or "No summary generated.")

    add_heading("Key Decisions", 1)
    if decisions:
        for d in decisions:
            p = doc.add_paragraph(d, style="List Bullet")
    else:
        add_body("No decisions extracted.")

    add_heading("Action Items", 1)
    if action_items:
        tbl = doc.add_table(rows=1, cols=5)
        tbl.style = "Table Grid"
        hdr = tbl.rows[0].cells
        for i, h in enumerate(["#", "Action", "Assignee", "Deadline", "Status"]):
            hdr[i].text = h
        for idx, a in enumerate(action_items, 1):
            row = tbl.add_row().cells
            row[0].text = str(idx)
            row[1].text = a.get("text", "")[:100]
            row[2].text = a.get("assignee_name") or "Unassigned"
            row[3].text = str(a.get("deadline") or "—")
            row[4].text = (a.get("status", "pending")).title()
    else:
        add_body("No action items.")

    add_heading("Full Transcript", 1)
    add_body(transcript_text or "No transcript available.")

    doc.add_paragraph()
    footer_p = doc.add_paragraph(
        f"Generated by Online Meeting Summarizer · {datetime.now().strftime('%d %B %Y %H:%M')}"
    )
    for run in footer_p.runs:
        run.font.color.rgb = GRAY
        run.font.size = Pt(8)

    buf = io.BytesIO()
    doc.save(buf)
    return buf.getvalue()
