import io
import os
from datetime import datetime
from typing import List, Optional, Any


def _format_date(dt: Any) -> str:
    if dt is None:
        return "Unknown Date"
    if isinstance(dt, str):
        try:
            dt = datetime.fromisoformat(dt)
        except Exception:
            return dt
    return dt.strftime("%d %B %Y")


# ------------------------------------------------------------------
# TXT export
# ------------------------------------------------------------------

def export_txt(
    meeting_title: str,
    meeting_date: Any,
    participants: List[str],
    summary_text: str,
    decisions: List[str],
    action_items: List[dict],
    transcript_text: str,
) -> bytes:
    lines = [
        "=" * 72,
        f"MEETING MINUTES",
        f"Starlight Secondary School, Amuria District, Uganda",
        "=" * 72,
        f"Title      : {meeting_title}",
        f"Date       : {_format_date(meeting_date)}",
        f"Attendees  : {', '.join(participants) if participants else 'N/A'}",
        "",
        "EXECUTIVE SUMMARY",
        "-" * 40,
        summary_text or "No summary generated.",
        "",
        "KEY DECISIONS",
        "-" * 40,
    ]
    for i, d in enumerate(decisions, 1):
        lines.append(f"  {i}. {d}")
    if not decisions:
        lines.append("  No decisions extracted.")

    lines += [
        "",
        "ACTION ITEMS",
        "-" * 40,
    ]
    for i, a in enumerate(action_items, 1):
        assignee = a.get("assignee_name") or "Unassigned"
        deadline = a.get("deadline") or "No deadline"
        status = a.get("status", "pending").title()
        lines.append(f"  {i}. [{status}] {a.get('text', '')} — {assignee} (Due: {deadline})")
    if not action_items:
        lines.append("  No action items.")

    lines += [
        "",
        "FULL TRANSCRIPT",
        "-" * 40,
        transcript_text or "No transcript available.",
        "",
        "=" * 72,
        f"Generated by Online Meeting Summarizer on {datetime.now().strftime('%d %B %Y %H:%M')}",
        "=" * 72,
    ]
    return "\n".join(lines).encode("utf-8")


